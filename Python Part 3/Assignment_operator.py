x = int(input("Enter 1st Number : "))
y = int(input("Enter 2nd Number : "))

x += y
print("After += :", x)

x -= y
print("After -= :", x)

x *= y
print("After *= :", x)

x /= y
print("After /= :", x)

x %= y
print("After %= :", x)

x **= y
print("After **= :", x)

x //= y
print("After //= :", x)